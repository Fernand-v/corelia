CREATE TABLE code_catalog_domains (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  key text NOT NULL UNIQUE,
  display_name text NOT NULL
);

CREATE TABLE code_catalog_fields (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  domain_id uuid NOT NULL REFERENCES code_catalog_domains(id) ON DELETE CASCADE,
  key text NOT NULL,
  display_name text NOT NULL,
  UNIQUE (domain_id, key)
);

CREATE TABLE code_catalog_entries (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  field_id uuid NOT NULL REFERENCES code_catalog_fields(id) ON DELETE CASCADE,
  code integer GENERATED BY DEFAULT AS IDENTITY,
  key text NOT NULL,
  label text NOT NULL,
  description text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (field_id, code),
  UNIQUE (field_id, key)
);

