-- Corelia 3NF strict proposal
-- Inspired by apps/api/prisma/schema.prisma, but not a migration of the current DB.
-- Goals:
--   * snake_case table and column names
--   * no arrays
--   * no JSON payload columns
--   * no cache/derived columns such as current_version, sync_teams_count, progress_pct
--   * no polymorphic "many nullable FK" columns; use specific link/target tables
--   * lookup/status values stored in catalog tables instead of enum-only columns

CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- ============================================================
-- Identity, RBAC and organizational structure
-- ============================================================

CREATE TABLE permission_categories (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code integer GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  key text NOT NULL UNIQUE,
  display_name text NOT NULL,
  description text,
  sort_order integer NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE programs (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code integer GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  key text NOT NULL UNIQUE,
  display_name text NOT NULL,
  description text,
  sort_order integer NOT NULL DEFAULT 0,
  is_system boolean NOT NULL DEFAULT false,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE permissions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code integer GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  key text NOT NULL UNIQUE,
  display_name text NOT NULL,
  description text,
  program_id uuid NOT NULL REFERENCES programs(id) ON DELETE RESTRICT,
  category_id uuid NOT NULL REFERENCES permission_categories(id) ON DELETE RESTRICT,
  is_system boolean NOT NULL DEFAULT false,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE role_scopes (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  key text NOT NULL UNIQUE,
  display_name text NOT NULL
);

CREATE TABLE roles (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code integer GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  key text NOT NULL UNIQUE,
  display_name text NOT NULL,
  description text,
